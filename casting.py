# Python Casting
#Integers:

x = int(1)   # x will be 1
y = int(2.8) # y will be 2
z = int("3") # z will be 3

#Floats:

f = float(1)     # f will be 1.0
d = float(2.8)   # d will be 2.8
l = float("3")   # l will be 3.0
w = float("4.2") # w will be 4.2

#Strings:

b = str("s1") # b will be 's1'
g = str(2)    # g will be '2'
t = str(3.0)  # t will be '3.0'